// ─── Main export ────────────────────────────────────────────────────────────

/**
 * Generate a complete HTML report from the AI analysis and crawl data.
 * Returns a self-contained HTML string ready to serve.
 */
export function buildReport(
  audit: AuditRow,
  analysis: AnalysisResult,
  scorecard: Scorecard,
  consentData: ConsentResult | null
): string {
  const crawlPages = getCrawlPages(audit.id).filter(p => p.page_type !== 'consent_check');
  const auditDate = new Date(audit.created_at).toLocaleDateString('en-IE', {
    day: 'numeric', month: 'long', year: 'numeric',
  });

  // Group issues by category
  const issuesByCategory: Record<string, AnalysisIssue[]> = {};
  for (const issue of analysis.issues) {
    const cat = issue.category || 'Other';
    if (!issuesByCategory[cat]) issuesByCategory[cat] = [];
    issuesByCategory[cat].push(issue);
  }

  // Sort categories: put categories with critical issues first
  const severityRank: Record<string, number> = { critical: 4, high: 3, medium: 2, low: 1 };
  const sortedCategories = Object.entries(issuesByCategory).sort((a, b) => {
    const aMax = Math.max(...a[1].map(i => severityRank[i.severity] || 0));
    const bMax = Math.max(...b[1].map(i => severityRank[i.severity] || 0));
    return bMax - aMax;
  });

  // Get homepage screenshot
  const homepageCrawl = crawlPages.find(p => p.page_type === 'homepage');
  const homepageScreenshot = homepageCrawl?.screenshot_path || null;

  // Count issues for dynamic CTA
  const totalIssues = analysis.issues.length;
  const criticalOrHigh = analysis.issues.filter(i => i.severity === 'critical' || i.severity === 'high').length;

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Tracking Health Audit — ${escHtml(audit.website_url)} | TrackGuard</title>
  <style>${reportCSS()}</style>
</head>
<body>
  <div class="report">
    <!-- Header -->
    ${renderHeader(audit, auditDate)}

    <!-- How to read this report -->
    <div class="how-to-read">
      This report was generated by an automated crawl of your public website. No access to your GA4 property or tag manager was required. All findings are based on externally observable tracking behaviour.
    </div>

    <!-- Scorecard -->
    ${renderScorecard(scorecard, analysis)}

    <!-- Data Impact (promoted to top) -->
    ${analysis.data_impact_summary ? renderDataImpact(analysis.data_impact_summary) : ''}

    <!-- Table of Contents -->
    ${renderTableOfContents(consentData)}

    <!-- Executive Summary -->
    ${renderExecutiveSummary(analysis)}

    <!-- Consent Analysis -->
    ${consentData ? renderConsentSection(consentData) : ''}

    <!-- Homepage Screenshot -->
    ${homepageScreenshot ? renderScreenshot(homepageScreenshot, 'Homepage at time of audit') : ''}

    <!-- Findings -->
    ${renderFindings(sortedCategories, crawlPages)}

    <!-- Positive Findings -->
    ${renderPositiveFindings(analysis.positive_findings)}

    <!-- Pages Crawled -->
    ${renderPagesSummary(crawlPages)}

    <!-- Limitations -->
    ${renderLimitations(analysis.limitations)}

    <!-- Footer -->
    ${renderFooter(audit, totalIssues, criticalOrHigh)}
  </div>
</body>
</html>`;
}

