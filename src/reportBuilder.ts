function renderConsentSection(consent: ConsentResult): string {
  if (!consent.bannerFound && !consent.defaultConsentState) {
    return `
    <section class="section" id="consent-analysis">
      <h2>Consent & Privacy</h2>
      <div class="alert alert-warning">
        <strong>No consent banner detected.</strong> The crawler did not find a recognisable consent management banner on the homepage. If this site serves EU visitors, this may be a GDPR compliance gap.
      </div>
    </section>`;
  }

  const consentKeys = ['ad_storage', 'analytics_storage', 'ad_user_data', 'ad_personalization',
    'functionality_storage', 'personalization_storage', 'security_storage'];

  const defaultState = consent.defaultConsentState || {};
  const postState = consent.postConsentState || {};

  // Filter to only consent-relevant keys
  const relevantDefault = Object.fromEntries(
    Object.entries(defaultState).filter(([k]) => consentKeys.includes(k))
  );
  const relevantPost = Object.fromEntries(
    Object.entries(postState).filter(([k]) => consentKeys.includes(k))
  );

  // Group post-consent GA4 requests: group unresolved events together
  const resolvedRequests: { label: string; measId: string }[] = [];
  const unresolvedByMeasId: Record<string, number> = {};
  for (const r of consent.postConsentGA4Requests) {
    const eventLabel = (r.event_names || []).join(', ');
    const measId = r.measurement_id || 'unknown ID';
    if (eventLabel && eventLabel !== '') {
      resolvedRequests.push({ label: eventLabel, measId });
    } else {
      unresolvedByMeasId[measId] = (unresolvedByMeasId[measId] || 0) + 1;
    }
  }

  return `
    <section class="section" id="consent-analysis">
      <h2>Consent & Privacy Analysis</h2>

      <div class="consent-meta">
        <span class="consent-badge">${escHtml(consent.cmpDetected || 'Unknown')} CMP</span>
        ${consent.acceptButtonClicked
          ? '<span class="consent-badge consent-ok">Accept All — Clicked ✓</span>'
          : '<span class="consent-badge consent-warn">Accept button not found</span>'}
      </div>

      ${consent.bannerScreenshot ? renderScreenshot(consent.bannerScreenshot, 'Consent banner as displayed to visitors') : ''}

      <h3>Before vs After Consent</h3>
      <div class="consent-table-wrap">
        <table class="consent-table">
          <thead>
            <tr>
              <th>Consent Parameter</th>
              <th>Default (Before)</th>
              <th>After Accept All</th>
              <th>Assessment</th>
            </tr>
          </thead>
          <tbody>
            ${consentKeys.map(key => {
              const before = relevantDefault[key] || '—';
              const after = relevantPost[key] || '—';
              let status = '';
              let statusClass = '';
              if (before === 'denied' && after === 'granted') {
                status = '✓ Correct';
                statusClass = 'consent-cell-ok';
              } else if (before === 'denied' && after === 'denied') {
                status = '⚠ Still denied';
                statusClass = 'consent-cell-warn';
              } else if (before === 'granted' && after === 'granted') {
                status = '⚠ No default deny';
                statusClass = 'consent-cell-warn';
              } else if ((before === '—' || before === 'undefined' || !before) && (after === '—' || after === 'undefined' || !after)) {
                status = 'Not configured';
                statusClass = 'consent-cell-missing';
              } else if ((before === '—' || before === 'undefined' || !before) && after === 'granted') {
                status = '⚠ No default denied state';
                statusClass = 'consent-cell-warn';
              } else if ((before === '—' || before === 'undefined' || !before) && after === 'denied') {
                status = '⚠ Not configured';
                statusClass = 'consent-cell-warn';
              } else {
                status = 'Inconclusive';
                statusClass = 'consent-cell-missing';
              }
              return `<tr>
                <td><code>${escHtml(key)}</code></td>
                <td class="${before === 'denied' ? 'consent-cell-denied' : before === 'granted' ? 'consent-cell-granted' : 'consent-cell-missing'}">${(!before || before === '—' || before === 'undefined') ? 'Not set' : escHtml(before)}</td>
                <td class="${after === 'denied' ? 'consent-cell-denied' : after === 'granted' ? 'consent-cell-granted' : 'consent-cell-missing'}">${(!after || after === '—' || after === 'undefined') ? 'Not set' : escHtml(after)}</td>
                <td class="${statusClass}">${status}</td>
              </tr>`;
            }).join('')}
          </tbody>
        </table>
      </div>

      ${consent.postConsentGA4Requests.length > 0 ? `
      <h3>Tags That Fired After Consent</h3>
      <p class="muted">These ${consent.postConsentGA4Requests.length} GA4/Google requests fired after the user clicked "Accept All" — confirming they are correctly gated behind consent.</p>
      <ul class="tag-list">
        ${resolvedRequests.slice(0, 10).map(r =>
          `<li><code>${escHtml(r.label)}</code> → ${escHtml(r.measId)}</li>`
        ).join('')}
        ${Object.entries(unresolvedByMeasId).map(([measId, count]) =>
          `<li><code>${count} unresolved event${count > 1 ? 's' : ''}</code> → ${escHtml(measId)}</li>`
        ).join('')}
        ${resolvedRequests.length > 10 ? `<li class="muted">…and ${resolvedRequests.length - 10} more named events</li>` : ''}
      </ul>` : ''}
    </section>`;
}

function renderFindings(
  sortedCategories: [string, AnalysisIssue[]][],
  crawlPages: any[]
): string {
  if (sortedCategories.length === 0) {
    return `
    <section class="section" id="detailed-findings">
      <h2>Detailed Findings</h2>
      <p>No issues were identified during this audit.</p>
    </section>`;
  }

  // Build a global issue counter across all categories
  let issueNumber = 0;

  return `
    <section class="section" id="detailed-findings">
      <h2>Detailed Findings</h2>
      ${sortedCategories.map(([category, issues]) => `
        <div class="category-group">
          <h3 class="category-title">${escHtml(category)}</h3>
          ${issues.map(issue => {
            issueNumber++;
            return renderIssueCard(issue, issueNumber);
          }).join('')}
        </div>
      `).join('')}
    </section>`;
}

