function renderConsentSection(consent: ConsentResult): string {
  if (!consent.bannerFound && !consent.defaultConsentState) {
    return `
    <section class="section" id="consent-analysis">
      <h2>Consent & Privacy</h2>
      <div class="alert alert-warning">
        <strong>No consent banner detected.</strong> The crawler did not find a recognisable consent management banner on the homepage. If this site serves EU visitors, this may be a GDPR compliance gap.
      </div>
    </section>`;
  }

  const consentKeys = ['ad_storage', 'analytics_storage', 'ad_user_data', 'ad_personalization',
    'functionality_storage', 'personalization_storage', 'security_storage'];

  const defaultState = consent.defaultConsentState || {};
  const postState = consent.postConsentState || {};

  // Filter to only consent-relevant keys
  const relevantDefault = Object.fromEntries(
    Object.entries(defaultState).filter(([k]) => consentKeys.includes(k))
  );
  const relevantPost = Object.fromEntries(
    Object.entries(postState).filter(([k]) => consentKeys.includes(k))
  );

  // Group post-consent GA4 requests: group unresolved events together
  const resolvedRequests: { label: string; measId: string }[] = [];
  const unresolvedByMeasId: Record<string, number> = {};
  for (const r of consent.postConsentGA4Requests) {
    const eventLabel = (r.event_names || []).join(', ');
    const measId = r.measurement_id || 'unknown ID';
    if (eventLabel && eventLabel !== '') {
      resolvedRequests.push({ label: eventLabel, measId });
    } else {
      unresolvedByMeasId[measId] = (unresolvedByMeasId[measId] || 0) + 1;
    }
  }

  return `
    <section class="section" id="consent-analysis">
      <h2>Consent & Privacy Analysis</h2>

      <div class="consent-meta">
        <span class="consent-badge">${escHtml(consent.cmpDetected || 'Unknown')} CMP</span>
        ${consent.acceptButtonClicked
          ? '<span class="consent-badge consent-ok">Accept All — Clicked ✓</span>'
          : '<span class="consent-badge consent-warn">Accept button not found</span>'}
      </div>

      ${consent.bannerScreenshot ? renderScreenshot(consent.bannerScreenshot, 'Consent banner as displayed to visitors') : ''}

      <h3>Before vs After Consent</h3>
      <div class="consent-table-wrap">
        <table class="consent-table">
          <thead>
            <tr>
              <th>Consent Parameter</th>
              <th>Default (Before)</th>
              <th>After Accept All</th>
              <th>Assessment</th>
            </tr>
          </thead>
          <tbody>
            ${consentKeys.map(key => {
              const before = relevantDefault[key] || '—';
              const after = relevantPost[key] || '—';
              let status = '';
              let statusClass = '';
              if (before === 'denied' && after === 'granted') {
                status = '✓ Correct';
                statusClass = 'consent-cell-ok';
              } else if (before === 'denied' && after === 'denied') {
                status = '⚠ Still denied';
                statusClass = 'consent-cell-warn';
              } else if (before === 'granted' && after === 'granted') {
                status = '⚠ No default deny';
                statusClass = 'consent-cell-warn';
              } else if (before === '—' && after === '—') {
                status = '— Not set';
                statusClass = 'consent-cell-missing';
              } else {
                status = '?';
                statusClass = '';
              }
              return `<tr>
                <td><code>${escHtml(key)}</code></td>
                <td class="${before === 'denied' ? 'consent-cell-denied' : before === 'granted' ? 'consent-cell-granted' : ''}">${escHtml(before)}</td>
                <td class="${after === 'denied' ? 'consent-cell-denied' : after === 'granted' ? 'consent-cell-granted' : ''}">${escHtml(after)}</td>
                <td class="${statusClass}">${status}</td>
              </tr>`;
            }).join('')}
          </tbody>
        </table>
      </div>

      ${consent.postConsentGA4Requests.length > 0 ? `
      <h3>Tags That Fired After Consent</h3>
      <p class="muted">These ${consent.postConsentGA4Requests.length} GA4/Google requests fired after the user clicked "Accept All" — confirming they are correctly gated behind consent.</p>
      <ul class="tag-list">
        ${resolvedRequests.slice(0, 10).map(r =>
          `<li><code>${escHtml(r.label)}</code> → ${escHtml(r.measId)}</li>`
        ).join('')}
        ${Object.entries(unresolvedByMeasId).map(([measId, count]) =>
          `<li><code>${count} unresolved event${count > 1 ? 's' : ''}</code> → ${escHtml(measId)}</li>`
        ).join('')}
        ${resolvedRequests.length > 10 ? `<li class="muted">…and ${resolvedRequests.length - 10} more named events</li>` : ''}
      </ul>` : ''}
    </section>`;
}

function renderFindings(
  sortedCategories: [string, AnalysisIssue[]][],
  crawlPages: any[]
): string {
  if (sortedCategories.length === 0) {
    return `
    <section class="section" id="detailed-findings">
      <h2>Detailed Findings</h2>
      <p>No issues were identified during this audit.</p>
    </section>`;
  }

  // Build a global issue counter across all categories
  let issueNumber = 0;

  return `
    <section class="section" id="detailed-findings">
      <h2>Detailed Findings</h2>
      ${sortedCategories.map(([category, issues]) => `
        <div class="category-group">
          <h3 class="category-title">${escHtml(category)}</h3>
          ${issues.map(issue => {
            issueNumber++;
            return renderIssueCard(issue, issueNumber);
          }).join('')}
        </div>
      `).join('')}
    </section>`;
}

function renderIssueCard(issue: AnalysisIssue, issueNumber: number): string {
  return `
    <div class="issue-card issue-${issue.severity}">
      <div class="issue-header">
        <span class="issue-number">Issue ${issueNumber}</span>
        <span class="severity-badge badge-${issue.severity}">${issue.severity.toUpperCase()}</span>
        <h4 class="issue-title">${escHtml(issue.title)}</h4>
      </div>
      <div class="issue-body">
        <div class="issue-section">
          <div class="issue-label">Evidence</div>
          <p>${escHtml(issue.evidence)}</p>
        </div>
        <div class="issue-section">
          <div class="issue-label">Business Impact</div>
          <p>${escHtml(issue.business_impact)}</p>
        </div>
        <div class="issue-section">
          <div class="issue-label">Recommendation</div>
          <p>${escHtml(issue.recommendation)}</p>
        </div>
      </div>
    </div>`;
}

function renderPositiveFindings(findings: string[]): string {
  if (!findings || findings.length === 0) return '';

  return `
    <section class="section" id="whats-working">
      <h2>What's Working Well</h2>
      <ul class="positive-list">
        ${findings.map(f => `<li>${escHtml(f)}</li>`).join('')}
      </ul>
    </section>`;
}

function renderPagesSummary(crawlPages: any[]): string {
  return `
    <section class="section" id="pages-audited">
      <h2>Pages Audited</h2>
      <table class="pages-table">
        <thead>
          <tr>
            <th>Page Type</th>
            <th>URL</th>
            <th>Load Time</th>
            <th>DataLayer</th>
            <th>GA4 Hits</th>
          </tr>
        </thead>
        <tbody>
          ${crawlPages.map(p => {
            const dl = safeParseJSON(p.datalayer_events, []);
            const ga4 = safeParseJSON(p.ga4_requests, []);
            const shortUrl = p.page_url.length > 60 ? p.page_url.substring(0, 57) + '…' : p.page_url;
            return `<tr>
              <td><span class="page-type-badge">${escHtml(p.page_type)}</span></td>
              <td class="url-cell"><a href="${escHtml(p.page_url)}" target="_blank" rel="noopener" title="${escHtml(p.page_url)}">${escHtml(shortUrl)}</a></td>
              <td>${p.page_load_ms > 0 ? (p.page_load_ms / 1000).toFixed(1) + 's' : '—'}</td>
              <td>${dl.length}</td>
              <td>${ga4.length}</td>
            </tr>`;
          }).join('')}
        </tbody>
      </table>
    </section>`;
}

function renderLimitations(limitations: string[]): string {
  if (!limitations || limitations.length === 0) return '';

  return `
    <section class="section" id="limitations">
      <h2>Audit Limitations</h2>
      <p class="muted">The following aspects could not be fully verified during this automated crawl:</p>
      <ul class="limitations-list">
        ${limitations.map(l => `<li>${escHtml(l)}</li>`).join('')}
      </ul>
    </section>`;
}

function renderScreenshot(screenshotRelPath: string, caption: string): string {
  // Embed screenshot as base64 data URI so the report is fully self-contained
  const absolutePath = path.join(__dirname, '..', 'public', screenshotRelPath);
  try {
    if (fs.existsSync(absolutePath)) {
      const imageBuffer = fs.readFileSync(absolutePath);
      const base64 = imageBuffer.toString('base64');
      const mimeType = screenshotRelPath.endsWith('.png') ? 'image/png' : 'image/jpeg';
      return `
    <div class="screenshot-container">
      <img src="data:${mimeType};base64,${base64}" alt="${escHtml(caption)}" class="screenshot" loading="lazy" />
      <p class="screenshot-caption">${escHtml(caption)}</p>
    </div>`;
    }
  } catch { /* fall through to fallback */ }
  // Fallback: relative path (works when served from the Express static server)
  return `
    <div class="screenshot-container">
      <img src="/${escHtml(screenshotRelPath)}" alt="${escHtml(caption)}" class="screenshot" loading="lazy" />
      <p class="screenshot-caption">${escHtml(caption)}</p>
    </div>`;
}

function renderFooter(audit: AuditRow, totalIssues: number, criticalOrHigh: number): string {
  // Build a dynamic CTA line based on findings
  let ctaLine = 'Ask us about <strong>Release Guard</strong> — automated tracking checks after every site change. Never ship broken analytics again.';
  if (totalIssues > 0 && criticalOrHigh > 0) {
    ctaLine = `This audit found ${totalIssues} issue${totalIssues !== 1 ? 's' : ''}, including ${criticalOrHigh} rated critical or high severity. <strong>Release Guard</strong> would have caught these automatically before they affected your reporting. Ask us about ongoing monitoring.`;
  } else if (totalIssues > 0) {
    ctaLine = `This audit found ${totalIssues} issue${totalIssues !== 1 ? 's' : ''}. <strong>Release Guard</strong> monitors your tracking continuously so issues like these are caught before they impact your data. Ask us about ongoing monitoring.`;
  }

  return `
    <footer class="report-footer">
      <div class="footer-cta">
        <h3>Want Ongoing Monitoring?</h3>
        <p>${ctaLine}</p>
      </div>
      <div class="footer-meta">
        <div class="brand brand-sm">Track<span class="brand-accent">Guard</span></div>
        <p>Report generated ${new Date().toLocaleDateString('en-IE', { day: 'numeric', month: 'long', year: 'numeric' })} · Ref: ${escHtml(audit.reference_id)}</p>
      </div>
    </footer>`;
}

