function renderScorecard(scorecard: Scorecard, analysis: AnalysisResult): string {
  const scoreColor = scorecard.health_score >= 80 ? 'score-green'
    : scorecard.health_score >= 50 ? 'score-amber'
    : 'score-red';

  const totalIssues = scorecard.critical_count + scorecard.high_count + scorecard.medium_count + scorecard.low_count;

  return `
    <section class="scorecard">
      <div class="score-circle ${scoreColor}">
        <div class="score-number">${scorecard.health_score}</div>
        <div class="score-label">Health Score</div>
      </div>
      <div class="score-breakdown">
        <h2>Issue Summary</h2>
        <div class="severity-counts">
          ${scorecard.critical_count > 0 ? `<div class="sev-count sev-critical"><span class="sev-num">${scorecard.critical_count}</span><span class="sev-label">Critical</span></div>` : ''}
          ${scorecard.high_count > 0 ? `<div class="sev-count sev-high"><span class="sev-num">${scorecard.high_count}</span><span class="sev-label">High</span></div>` : ''}
          ${scorecard.medium_count > 0 ? `<div class="sev-count sev-medium"><span class="sev-num">${scorecard.medium_count}</span><span class="sev-label">Medium</span></div>` : ''}
          ${scorecard.low_count > 0 ? `<div class="sev-count sev-low"><span class="sev-num">${scorecard.low_count}</span><span class="sev-label">Low</span></div>` : ''}
          ${totalIssues === 0 ? `<div class="sev-count sev-none"><span class="sev-num">0</span><span class="sev-label">Issues</span></div>` : ''}
        </div>
        <div class="category-pills">
          ${Object.entries(scorecard.categories).map(([cat, data]) =>
            `<span class="cat-pill cat-${data.worst_severity}">${escHtml(cat)} (${data.issues})</span>`
          ).join('')}
        </div>
      </div>
    </section>`;
}

