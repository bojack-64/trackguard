// reportBuilder.ts — HTML Report Generator
// Takes the AI analysis output + crawl data and renders a polished,
// self-contained HTML report. This is the product — the report must
// stand on its own without explanation.

import { AuditRow, getCrawlPages } from './database';
import { AnalysisResult, Scorecard, AnalysisIssue } from './analyser';
import { ConsentResult } from './crawler';
import path from 'path';
